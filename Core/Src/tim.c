void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==TIM4)
	{
		button_ticks();
	}
	
	if(htim->Instance==TIM6)
	{
		float v = 0;
		float i = 0;
		v = actvol;
		i = actcurrent;
		LCD_ShowNum6432(23,7,v,4,GREEN);
		LCD_ShowNum6432(23,71,i,4,YELLOW);
		LCD_ShowNum6432(23,135,i*v,4,LIGHTGREEN);
	}
}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  if(htim->Instance==TIM3)//判断是否是定时器3
	{
		if(htim->Channel==HAL_TIM_ACTIVE_CHANNEL_1)//判断是否是通道1触发
		{
			if(__HAL_TIM_IS_TIM_COUNTING_DOWN(htim))//判断DIR，计数方向向下
			{
				if(state == 1)
				{
					unsigned char temp = set[i];
					if(temp > 0)
					{
						temp--;
						set[i] = temp;
						LCD_ShowChar(pos[i],207,set[i]+48,0,MAGENTA);
					}
					else
					{
						set[i] = 9;
						LCD_ShowChar(pos[i],207,set[i]+48,0,MAGENTA);
					}
				}
			}
		}
		else if(htim->Channel==HAL_TIM_ACTIVE_CHANNEL_2)
		{
			if(!__HAL_TIM_IS_TIM_COUNTING_DOWN(htim))
			{
				if(state == 1)
				{
					unsigned char temp = set[i];
					if(temp < 9)
					{
						temp++;
						set[i] = temp;
						LCD_ShowChar(pos[i],207,set[i]+48,0,MAGENTA);
					}
					else
					{
						set[i] = 0;
						LCD_ShowChar(pos[i],207,set[i]+48,0,MAGENTA);
					}
				}
			}
		}
	}
}
